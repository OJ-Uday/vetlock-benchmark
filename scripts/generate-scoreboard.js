#!/usr/bin/env node
/**
 * generate-scoreboard.js — read every results/*.json and produce docs/SCOREBOARD.md.
 *
 * Every scoreboard row is regenerated from raw results — no hand-edits. Drift is
 * caught by scoreboard-drift.test (a CI check).
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(HERE, '..');
const RESULTS_DIR = path.join(REPO_ROOT, 'results');
const OUT = path.join(REPO_ROOT, 'docs', 'SCOREBOARD.md');

if (!existsSync(RESULTS_DIR)) {
  console.error('no results/ yet — run `node scripts/run-benchmark.js --scanner all` first');
  process.exit(2);
}

const files = readdirSync(RESULTS_DIR).filter((f) => f.endsWith('.json'));
if (files.length === 0) {
  console.error('no result files under results/');
  process.exit(2);
}

// Group by scanner — keep the most recent result per scanner.
const latest = new Map(); // scanner → parsed
for (const f of files) {
  const parsed = JSON.parse(readFileSync(path.join(RESULTS_DIR, f), 'utf8'));
  const existing = latest.get(parsed.scanner);
  if (!existing || parsed.date > existing.date) latest.set(parsed.scanner, parsed);
}

const scanners = [...latest.values()].sort((a, b) => b.summary.score - a.summary.score);

const lines = [];
lines.push('# Vetlock Benchmark — Scoreboard');
lines.push('');
lines.push(`**Generated:** ${new Date().toISOString().slice(0, 10)} · **Corpus version:** v0.1`);
lines.push('');
lines.push('> Every scanner is scored against the same defanged corpus by the same harness.');
lines.push('> Rules: [README.md](../README.md#what-caught-means-the-scoring-rules).');
lines.push('> This document is auto-generated by `scripts/generate-scoreboard.js` — do not edit by hand.');
lines.push('');

// Summary table
lines.push('## Summary');
lines.push('');
lines.push('| Scanner | Score | Caught | Partial | Missed | Errored | Last run |');
lines.push('|---|---:|---:|---:|---:|---:|---|');
for (const s of scanners) {
  const c = s.summary.counts;
  const scorePct = (s.summary.score * 100).toFixed(1);
  lines.push(`| \`${s.scanner}\` | **${scorePct}%** | ${c.caught} | ${c.partial} | ${c.missed} | ${c.errored} | ${s.date} |`);
}
lines.push('');

// Per-fixture breakdown — one row per fixture, one column per scanner
const allFixtures = new Set();
for (const s of scanners) for (const r of s.results) allFixtures.add(r.fixtureId);
const fixtureIds = [...allFixtures].sort();

lines.push('## Per-fixture breakdown');
lines.push('');
lines.push('| Fixture | ' + scanners.map((s) => `\`${s.scanner}\``).join(' | ') + ' |');
lines.push('|---|' + scanners.map(() => ':---:').join('|') + '|');
const glyph = { caught: '✓', partial: '⚠', missed: '✗', errored: '!' };
for (const fid of fixtureIds) {
  const row = [`\`${fid}\``];
  for (const s of scanners) {
    const r = s.results.find((x) => x.fixtureId === fid);
    row.push(r ? `${glyph[r.verdict] || '?'} ${r.verdict}` : '—');
  }
  lines.push('| ' + row.join(' | ') + ' |');
}
lines.push('');

// Honesty section
lines.push('## Honesty notes');
lines.push('');
lines.push('- **All scanners are run against ALL fixtures.** A scanner that\'s not installed in the run environment records `errored`, not `skipped`. That\'s the truth.');
lines.push('- **`caught`** = the scanner returned non-CLEAN AND at least one finding is on the malicious package.');
lines.push('- **`partial`** = non-CLEAN but findings are on unrelated packages (adjacency false-positive that happens to hit the affected version).');
lines.push('- **`missed`** = CLEAN or non-CLEAN on wrong packages entirely.');
lines.push('- **`errored`** = unhandled crash. A graceful "unsupported format" is `missed`, not `errored`.');
lines.push('- **vetlock is scored by the same rules as every other scanner.** vetlock\'s misses appear here too.');
lines.push('');
lines.push('If your scanner is missing or scored badly and you think that\'s wrong, [open an issue](https://github.com/OJ-Uday/vetlock-benchmark/issues) or send a PR against `scorers/<your-scanner>.js`.');

mkdirSync(path.dirname(OUT), { recursive: true });
writeFileSync(OUT, lines.join('\n') + '\n');
console.log(`wrote ${path.relative(REPO_ROOT, OUT)} (${scanners.length} scanner(s), ${fixtureIds.length} fixture(s))`);
